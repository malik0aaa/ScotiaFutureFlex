models.Base.metadata.create_all(bind=engine)


def _ensure_user_goal_target_amount_column() -> None:
    inspector = inspect(engine)
    columns = {column["name"] for column in inspector.get_columns("users")}
    if "goal_target_amount" not in columns:
        with engine.begin() as connection:
            connection.execute(text("ALTER TABLE users ADD COLUMN goal_target_amount FLOAT"))


def _latest_goal_for_user(db: Session, user_id: int) -> models.InvestmentGoal | None:
    return db.query(models.InvestmentGoal).filter(models.InvestmentGoal.user_id == user_id).order_by(
        models.InvestmentGoal.created_at.desc(),
        models.InvestmentGoal.id.desc(),
    ).first()


def _ensure_investment_goals_schema() -> None:
    inspector = inspect(engine)
    if not inspector.has_table("investment_goals"):
        return

    unique_constraints = inspector.get_unique_constraints("investment_goals")
    needs_rebuild = any("user_id" in constraint.get("column_names", []) for constraint in unique_constraints)
    if not needs_rebuild:
        return

    with engine.begin() as connection:
        connection.execute(text("ALTER TABLE investment_goals RENAME TO investment_goals_legacy"))
        models.InvestmentGoal.__table__.create(bind=connection, checkfirst=True)
        connection.execute(text(
            "INSERT INTO investment_goals (id, user_id, goal, target_amount, created_at, updated_at) "
            "SELECT id, user_id, goal, target_amount, created_at, updated_at FROM investment_goals_legacy"
        ))
        connection.execute(text("DROP TABLE investment_goals_legacy"))


def _sync_legacy_goals() -> None:
    db = SessionLocal()
    try:
        users = db.query(models.User).all()
        for user in users:
            goal_row = _latest_goal_for_user(db, user.id)
            if goal_row is None and user.goal:
                goal_row = models.InvestmentGoal(
                    user_id=user.id,
                    goal=user.goal,
                    target_amount=user.goal_target_amount,
                )
                db.add(goal_row)
            elif goal_row is not None:
                user.goal = goal_row.goal
                user.goal_target_amount = goal_row.target_amount
        db.commit()
    finally:
        db.close()


_ensure_user_goal_target_amount_column()
_ensure_investment_goals_schema()
_sync_legacy_goals()

